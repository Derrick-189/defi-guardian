    def verify_with_lean(self, lean_script):
        """Run Lean verification"""
        if not self.lean_available:
            return {
                'success': False,
                'error': 'Lean not installed. Please install Lean theorem prover.',
                'output': ''
            }
        
        try:
            # Create temporary .lean file
            with tempfile.NamedTemporaryFile(mode='w', suffix='.lean', delete=False) as f:
                f.write(lean_script)
                temp_file = f.name
            
            # Run Lean verification
            result = subprocess.run(
                ["lean", temp_file],
                capture_output=True,
                text=True,
                timeout=60
            )
            
            # Clean up
            os.unlink(temp_file)
            
            return {
                'success': result.returncode == 0,
                'output': result.stdout,
                'errors': result.stderr
            }
        except Exception as e:
            return {
                'success': False,
                'error': str(e),
                'output': ''
            }
